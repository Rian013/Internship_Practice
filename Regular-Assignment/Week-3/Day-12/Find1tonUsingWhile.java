import java.util.*;
class Find1tonUsingWhile {    
  public static void main(String args[]){      
    int n,i=1;     
    Scanner sc=new Scanner(System.in);      
    System.out.println("Enter n value");     
    n=sc.nextInt();      
    while(i<=n){        
      System.out.println(i);  
      i++;
    }         
  } 
}